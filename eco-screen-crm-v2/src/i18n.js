function normalizedChoiceKey(value) {
  return String(value ?? "").trim().toLowerCase().replace(/[\s-]+/g, "_");
}

function normalizedAliasValue(value, aliases) {
  const raw = String(value ?? "").trim();
  if (!raw) return "";
  const key = normalizedChoiceKey(raw);
  return aliases.get(key) || aliases.get(key.replace(/_/g, "")) || aliases.get(raw) || raw;
}

export function normalizeOpeningDirection(value) {
  return normalizedAliasValue(value, openingDirectionAliases);
}

export function normalizeColor(value) {
  return normalizedAliasValue(value, colorAliases);
}

function legacyLabel(value) {
  const raw = String(value ?? "").trim();
  return raw ? `${t("Legacy")}: ${raw}` : "-";
}

